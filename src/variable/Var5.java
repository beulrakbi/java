package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 선언, 초기화 각각 따로

        int a;
        a=1;
        System.out.println("a = " + a);

        int b =2; //변수 선언과 초기화를 한 번에
        System.out.println("b = " + b);

        //여러 변수 초기화 선언
        int c = 3, d =4;
        System.out.println("c = " + c);
        System.out.println("c = " + c);
    }
}
